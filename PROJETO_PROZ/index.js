/* Defina a largura da navegação lateral para 250px e a margem esquerda do conteúdo da página para 250px*/
function openNav() {
    document.getElementById("mySidenav").style.width = "250px";
    document.getElementById("main").style.marginLeft = "250px";
  }
  
  /* Defina a largura da navegação lateral como 0 e a margem esquerda do conteúdo da página como 0 */


  function closeNav() {
    document.getElementById("mySidenav").style.width = "0";
    document.getElementById("main").style.marginLeft = "0";
  }

